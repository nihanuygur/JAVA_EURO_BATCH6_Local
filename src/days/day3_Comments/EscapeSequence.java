package days.day3_Comments;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Spain\nItalyEnglandNorway");
        // insert a new line
        System.out.println("Spain\tItaly\tEngland\tNorway");
        // put a tab
        System.out.println("Spain\bItaly\tEngland\tNorway");
        // put a backslash
        System.out.println("Ali said that : \"Come Here\"");
        System.out.println("Ali said that : \\\\Come Here\\\\"); // for each one we need one  \ slash
        System.out.println("Hello World");

        byte sjhjkh = 120;
        byte sdsjhjkh = 120;

        int num1= 33 ;
        int num2= 22 ;
        // 11+22=33
        System.out.println(num1 + "+" + num2 +"=" + (num1+num2));

    }
}
